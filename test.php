        <h1 class="title">Les Arts du Cirque</h1>
        <p>Les Arts du Cirque mèlent des compétences sportives et artistiques où
            s'alternent travail individuel et travail collectif.<br/>
            <br/>
            Les apprentissages passent par des phases :</br>
            - d'observation où les enfants essaient de comprendre, reproduire un mouvement,</br>
            - d'explorations où les enfants vont chercher, tester et créer avec leur propre corps.<br/>
            <br/>

            Nous nous déplaçons dans votre école avec tout le matériel
            spécifique à la pratique des Arts du Cirque. <br/>
            <br/>

        <ul>
            <li>La jonglerie : assiettes chinoises, bâtons du diable, diabolos, balles, foulards...</li>
            <li>L'équilibre sur matériel : boule d'équilibre, rouleaux américains...</li>
            <li>L'acrobatie : roulades, pyramides...</li>
        </ul>


        <br/>
        <br/>

        En plus de toutes ces disciplines, une découverte du monde du spectacle avec des ateliers
        d'expression et pourquoi pas la création d'un spectacle.<br/>
        <br/>

        Le choix des techniques abordées se fait en fonction de l'âge des élèves et de la durée du projet.

        <h2>Les projets</h2>
        Quelques formules possibles  :

        <ul>
            <li>Découverte sur quelques scéances.</li>
            <li>Projet à plus long terme avec un montage de spectacle (10 scéances minimum).</li>
            <li>Cirque et musique en lien avec l'intervenante musique.</li>
        </ul>

        Et bien d'autres encore en fonction des envies des enseignants.


